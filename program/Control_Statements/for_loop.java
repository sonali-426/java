package Control_Statements;

import java.util.*;
public class for_loop {
    public static void main(String[] args){
        Scanner sc = new Scanner(System.in);
        System.out.println("enter  a no.");
        int a= sc.nextInt();
        for (int i  = 1 ; i <=10; i++) {
            System.out.println(a+ " * " +i +" = " + a*i);
            
        }
    }
}

    

